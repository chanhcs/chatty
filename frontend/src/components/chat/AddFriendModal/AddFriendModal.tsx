import { useState } from 'react';
import {
    Dialog,
    DialogContent,
    DialogHeader,
    DialogTitle,
    DialogTrigger,
} from "../../ui/dialog"; import { UserPlus } from 'lucide-react';
import type { User } from '@/types/store';
import { useFriendStore } from '@/stores/useFriendStore';
import { useForm } from 'react-hook-form';
import { toast } from 'sonner';
import SearchForm from '@/components/chat/AddFriendModal/SearchForm';
import SendFriendRequestForm from '@/components/chat/AddFriendModal/SendFriendRequestForm';

export interface IFormValues {
    username: string;
    message: string;
}

const AddFriendModal = () => {
    const [isFound, setIsFound] = useState<boolean | null>(null);
    const [searchUser, setSearchUser] = useState<User>();
    const [searchedUsername, setSearchedUsername] = useState("");
    const [isOpen, setIsOpen] = useState(false);
    const { loading, searchByUsername, addFriend } = useFriendStore();

    const {
        register,
        handleSubmit,
        watch,
        reset,
        formState: { errors },
    } = useForm<IFormValues>({
        defaultValues: { username: "", message: "" },
    });

    const usernameValue = watch("username");
    const handleCancel = () => {
        reset();
        setSearchedUsername("");
        setIsFound(null);
    };

    const closeModal = () => {
        handleCancel();
        setIsOpen(false);
    };

    const handleSearch = handleSubmit(async (data) => {
        const username = data.username.trim();
        if (!username) return;

        setIsFound(null);
        setSearchedUsername(username);

        try {
            const foundUser = await searchByUsername(username);
            if (foundUser) {
                setIsFound(true);
                setSearchUser(foundUser);
            } else {
                setIsFound(false);
            }
        } catch (error) {
            console.error(error);
            setIsFound(false);
        }
    });


    const handleSend = handleSubmit(async (data) => {
        if (!searchUser) return;
        try {
            const message = await addFriend(searchUser._id, data.message.trim());
            console.log('%cMyProject%cline:75%cmessage', 'color:#fff;background:#ee6f57;padding:3px;border-radius:2px', 'color:#fff;background:#1f3c88;padding:3px;border-radius:2px', 'color:#fff;background:rgb(3, 38, 58);padding:3px;border-radius:2px', message)
            toast.success(message);
            closeModal();
        } catch (error) {
            console.error(error);
        }
    });

    return (
        <Dialog open={isOpen} onOpenChange={setIsOpen}>
            <DialogTrigger asChild>
                <div className="flex justify-center items-center size-5 rounded-full hover:bg-sidebar-accent cursor-pointer z-10">
                    <UserPlus className="size-4" />
                    <span className="sr-only">Add Friend</span>
                </div>
            </DialogTrigger>

            <DialogContent className="sm:max-w-106.25 border-none">
                <DialogHeader>
                    <DialogTitle>Add Friend</DialogTitle>
                </DialogHeader>
                {!isFound && (
                    <>
                        <SearchForm
                            register={register}
                            errors={errors}
                            usernameValue={usernameValue}
                            loading={loading}
                            isFound={isFound}
                            searchedUsername={searchedUsername}
                            onSubmit={handleSearch}
                            onCancel={handleCancel}
                        />
                    </>
                )}

                {isFound && (
                    <>
                        <SendFriendRequestForm
                            register={register}
                            loading={loading}
                            searchedUsername={searchedUsername}
                            onSubmit={handleSend}
                            onBack={() => setIsFound(null)}
                        />
                    </>
                )}
            </DialogContent>
        </Dialog>
    );
};

export default AddFriendModal;